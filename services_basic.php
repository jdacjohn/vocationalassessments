                testing.</dd>
              <dt>Physical And Psychomotor Capacities</dt>
              <dd>The medical and physical assessment file information is
                reviewed and the evaluee is observed during testing to determine his/her level
                of functioning related to the Department of Labor physical demands of the
                vocational areas under consideration.</dd>
              <dt>Aptitudes</dt>
              <dd>The evaluee is assessed with standardized aptitude
                testing. His / Her overall performance on the vocational evaluation is used to
                determine his/her level of aptitude performance as related to the Department of
                Labor, Handbook on Analyzing Jobs. The aptitude information is utilized to
                identify vocational goals which are realistic with different levels of
                training.</dd>
              <dt>Achievements</dt>
              <dd>Various standardized academic and non-verbal tests are
                utilized to determine current achievement levels. The work history and academic
                levels of the individual are also assessed and this information is related to
                current achievements. If an evaluee is lacking in academic ability, and yet was
                able to function in a work capacity at a "higher level then he/she is tested to
                be", this is taken into consideration in the vocational recommendations.</dd>
              <dt>Evaluees Ability to Learn About Himself</dt>
              <dd>During the evaluation process the evaluation team will
                observe the evaluee in various situations which they have had no previous
                experiences. From this observation the ability to "learn from his/her mistakes"
                and his/her learning agility will be subjectively as well as objectively be
                noted.</dd>
              <dt>Learning Styles</dt>
              <dd>Every person learns differently with a combination of
                visual, auditory, modeling, and hands on techniques. The vocational evaluation
                will assess the primary method of learning that the evaluee may utilize to best
                be trained.</dd>
